package Cartas;
/*
 * Modela una carta del juego
 */
public class Carta {
	 String valor;
	 String categoría;
	public String getValor() {
		return valor;
	}
	public void setValor( String valor) {
		this.valor = valor;
	}
	public String getCategoría() {
		return categoría;
	}
	public void setCategoría(String categoría) {
		this.categoría = categoría;
	}

}
